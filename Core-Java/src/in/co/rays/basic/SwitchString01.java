package in.co.rays.basic;

public class SwitchString01 {
	public static void main(String[] args) {
		String fruit = "Banana";
		
		switch(fruit) {
		case"apple":
			System.out.println("this is red fruit");
			break;
		case"Banana":
			System.out.println("this is yellow fruit");
			break;
		}
	}

}
        