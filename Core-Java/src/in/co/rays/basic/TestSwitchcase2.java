package in.co.rays.basic;

public class TestSwitchcase2 {
	public static void main(String[] args) {
		String day = "Saturday";
		switch (day) {
		case "Monday":
			System.out.println("1 st day of the week");
			break;
		case "Tuesday":
			System.out.println("2 nd day of the week");
			break;
		case "Wednesday":
			System.out.println("3 nd day of the week");
			break;
		case "Thursday":
			System.out.println("4 nd day of the week");
			break;
		case "Friday":
			System.out.println("5 nd day of the week");
			break;
		case "Saturday":
			System.out.println("6 nd day of the week");
			break;
		default:
			System.out.println("Ajj chutti hai.....");
			break;
		}
	}

}
