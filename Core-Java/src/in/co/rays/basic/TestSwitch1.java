package in.co.rays.basic;

public class TestSwitch1 {
	public static void main(String[]args) {
	String m = "mar";
		switch (m){
			case"jan":
			    System.out.println("1 st moth");
			    break;
			case"feb":
				System.out.println("2 nd moth");
				break;
			case"mar":
				System.out.println("3 rd moth");
				break;
			default:
				System.out.println("this is default");
				break;
				
			
		}
	}

}
