package in.co.rays.basic;

public class Switchmonth1 {
	public static void main(String[]args) {
		String month = "Aug";
		switch (month) {
		case"Jan":
			System.out.println("1st month in year");
			break;
		case"Feb":
			System.out.println("2nd month in year");
			break;
		case"Mar":
			System.out.println("3rd month in year");
			break;
		case"Apr":
			System.out.println("4th month in year");
			break;
		case"May":
			System.out.println("5th month in year");
			break;
		case"Jun":
			System.out.println("6th month in year");
			break;
		case"Jul":
			System.out.println("7th month in year");
			break;
		case"Aug":
			System.out.println("8th month in year");
			break;
		case"Sep":
			System.out.println("9th month in year");
			break;
		case"Oct":
			System.out.println("10th month in year");
			break;
		case"Nov":
			System.out.println("11th month in year");
			break;
		case"Dec":
			System.out.println("12th month in year");
			break;
		default:
			System.out.println("NO month in year");
			break;
			}
	}

}
