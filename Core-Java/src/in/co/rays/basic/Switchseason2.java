package in.co.rays.basic;

public class Switchseason2 {
	public static void main(String[]args) {
		String season = "Varsha";
		switch(season) {
		case"Vasant":
			System.out.println("1st season in year");
			break;
		case"Grishma":
			System.out.println("2nd season in year");
		    break;
		case"Varsha":
			System.out.println("3rd season in year");
			break;
		case"sharad":
			System.out.println("4th season in year");
			break;
		case"Hemant":
			System.out.println("5th season in year");
			break;
		case"Shishir":
			System.out.println("6th season in year");
			break;
			default:
			System.out.println("No season");
			break;
		}
	}

}
